sbit LCD1602_RW = P1^4;
sbit LCD1602_E  = P1^5;

/* LCD1602显示的内容 */
extern unsigned char display1[16]; /* LCD1602第一行显示的内容 */
extern unsigned char display2[16]; /* LCD1602第二行显示的内容 */

/* 提供给其他C文件调用的函数 --------------------------------------------------------------------------------------------*/
