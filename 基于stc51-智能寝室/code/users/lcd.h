void LcdWriteData(unsigned char dat);  /* LCD1602写入8位数据子函数 */	
void LcdInit(void);								 		 /* LCD1602初始化 */		
void LcdDisplay(unsigned char disp1[16], unsigned char disp2[16]); /* LCD1602显示函数 */
void LCD_WriteDisplay(unsigned char x, unsigned char y, unsigned char dat);
void LCD_ClearLine(unsigned char line);

#endif /* __LCD_H */

